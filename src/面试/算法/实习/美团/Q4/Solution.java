import java.util.Scanner;

public class Solution {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        int[] p = new int[n];
        int[] a = new int[n];
        for (int i = 0; i < p.length; i++) {
            p[i]=sc.nextInt();
        }
        for (int i = 0; i < a.length; i++) {
            a[i]=sc.nextInt();
        }



    }
}
