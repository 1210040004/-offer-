package 面试.算法.实习.美团;


import java.util.Scanner;

/**
 * 删除一个数字以后最长上升字串的高度
 */
public class Solution2 {
    public static void main(String[] args) {
        Scanner sc= new Scanner(System.in);
        int n = sc.nextInt();
        int[] arr= new int[n];
        for (int i = 0; i < arr.length; i++) {
            arr[i]=sc.nextInt();
        }




    }

}
